// ── SVG TULIP ILLUSTRATION ──
// Returns an SVG string for a single illustrated tulip
// cx = center x, baseY = bottom of stem y, scale = size multiplier
window.tulipSVG = function(petals, stem, scale) {
  scale = scale || 1;
  var p1 = petals[0], p2 = petals[1];
  var s  = stem;
  // All coords relative, will be placed via transform on a <g>
  return [
    // Stem
    '<line x1="0" y1="0" x2="0" y2="70" stroke="' + s + '" stroke-width="' + (3*scale) + '" stroke-linecap="round"/>',
    // Leaf
    '<path d="M0 45 Q' + (-18*scale) + ' 30 ' + (-10*scale) + ' 15" fill="' + s + '" stroke="none"/>',
    // Left back petal
    '<ellipse cx="' + (-9*scale) + '" cy="' + (-22*scale) + '" rx="' + (9*scale) + '" ry="' + (18*scale) + '" fill="' + p2 + '" transform="rotate(-22 ' + (-9*scale) + ' ' + (-22*scale) + ')"/>',
    // Right back petal
    '<ellipse cx="' + (9*scale) + '" cy="' + (-22*scale) + '" rx="' + (9*scale) + '" ry="' + (18*scale) + '" fill="' + p2 + '" transform="rotate(22 ' + (9*scale) + ' ' + (-22*scale) + ')"/>',
    // Center front petal
    '<ellipse cx="0" cy="' + (-26*scale) + '" rx="' + (10*scale) + '" ry="' + (22*scale) + '" fill="' + p1 + '"/>',
    // Inner sheen
    '<ellipse cx="' + (-2*scale) + '" cy="' + (-28*scale) + '" rx="' + (3*scale) + '" ry="' + (8*scale) + '" fill="white" opacity="0.18"/>',
  ].join('');
};

// Build a launch-ready tulip DOM element (span wrapping an inline SVG)
window.makeTulipElement = function(variety, sizePx) {
  sizePx = sizePx || 80;
  var scale = sizePx / 80;
  var svgW = Math.round(40 * scale);
  var svgH = Math.round(90 * scale);
  var stemBase = Math.round(70 * scale);

  var el = document.createElement('span');
  el.style.cssText = 'display:inline-block;pointer-events:none;';
  el.innerHTML = '<svg width="' + svgW + '" height="' + svgH + '" viewBox="' + (-svgW/2) + ' ' + (-svgH + stemBase) + ' ' + svgW + ' ' + svgH + '" xmlns="http://www.w3.org/2000/svg">'
    + '<g transform="translate(0,' + (stemBase - Math.round(70*scale)) + ')">'
    + window.tulipSVG(variety.petals, variety.stem, scale)
    + '</g></svg>';
  return el;
};

// Build the variety grid HTML for the gift reveal section
window.buildVarietyGrid = function(page) {
  var varieties = window.TULIP_VARIETIES[page];
  if (!varieties) return '';

  var html = '<div style="display:grid;grid-template-columns:repeat(auto-fill,minmax(200px,1fr));gap:16px;margin-top:20px;">';
  varieties.forEach(function(v) {
    var svgW = 36, svgH = 80, scale = 0.9;
    var stemBase = Math.round(70 * scale);
    var svgStr = '<svg width="' + svgW + '" height="' + svgH + '" viewBox="' + (-svgW/2) + ' ' + (-svgH + stemBase) + ' ' + svgW + ' ' + svgH + '" xmlns="http://www.w3.org/2000/svg" style="flex-shrink:0;">'
      + '<g>' + window.tulipSVG(v.petals, v.stem, scale) + '</g></svg>';

    html += '<div style="background:#fff;border:1.5px solid ' + v.petals[0] + '33;border-radius:16px;padding:14px 16px;display:flex;flex-direction:column;gap:8px;">'
      + '<div style="display:flex;align-items:center;gap:10px;">'
      + svgStr
      + '<div>'
      + '<div style="font-family:\'Playfair Display\',serif;font-style:italic;font-size:0.88rem;font-weight:700;color:#1a1a1a;line-height:1.2;">' + v.name + '</div>'
      + '<div style="margin-top:3px;display:flex;gap:6px;flex-wrap:wrap;">'
      + '<span style="font-size:0.7rem;font-weight:800;letter-spacing:0.06em;background:' + v.petals[0] + '22;color:' + v.petals[1] + ';padding:2px 8px;border-radius:99px;border:1px solid ' + v.petals[0] + '44;">' + v.bloom + '</span>'
      + '<span style="font-size:0.7rem;font-weight:800;letter-spacing:0.06em;background:#f0f4f0;color:#3a6b30;padding:2px 8px;border-radius:99px;">' + v.height + '</span>'
      + '</div>'
      + '</div></div>'
      + '<p style="font-size:0.8rem;color:#555;line-height:1.55;margin:0;">' + v.desc + '</p>'
      + '</div>';
  });

  html += '</div>';
  return html;
};
